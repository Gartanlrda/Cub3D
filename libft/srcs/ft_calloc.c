#include "../include/libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*tab;

	if ((nmemb >= 65535 && size >= 65535) || (nmemb * size >= 65535))
		return (NULL);
	tab = malloc(nmemb * size);
	if (!tab)
		return (NULL);
	ft_bzero(tab, nmemb * size);
	return (tab);
}
